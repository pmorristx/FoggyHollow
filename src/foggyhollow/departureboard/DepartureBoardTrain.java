package foggyhollow.departureboard;

public class DepartureBoardTrain
{
    String roadName;
    String engineNumber;
    Boolean occupied;
    String destination;
    String trainName;
    String trainId;
    String trainNumber;
    String departureHour;
    String departureMinute;    
    
    public String getRoadName()
    {
        return roadName;
    }
    public void setRoadName(String roadName)
    {
        this.roadName = roadName;
    }
    public String getEngineNumber()
    {
        return engineNumber;
    }
    public void setEngineNumber(String number)
    {
        this.engineNumber = number;
    }
    public Boolean getOccupied()
    {
        return occupied;
    }
    public void setOccupied(Boolean occupied)
    {
        this.occupied = occupied;
    }
    public String getDestination()
    {
        return destination;
    }
    public void setDestination(String destination)
    {
        this.destination = destination;
    }
    public String getTrainName()
    {
        return trainName;
    }
    public void setTrainName(String trainName)
    {
        this.trainName = trainName;
    }
    public String getTrainId()
    {
        return trainId;
    }
    public void setTrainId(String trainId)
    {
        this.trainId = trainId;
    }
    public String getTrainNumber()
    {
        return trainNumber;
    }
    public void setTrainNumber(String trainNumber)
    {
        this.trainNumber = trainNumber;
    }
    public String getDepartureHour()
    {
        return departureHour;
    }
    public void setDepartureHour(String departureHour)
    {
        this.departureHour = departureHour;
    }
    public String getDepartureMinute()
    {
        return departureMinute;
    }
    public void setDepartureMinute(String departureMinute)
    {
        this.departureMinute = departureMinute;
    }

    
    
}
